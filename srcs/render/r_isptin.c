#include "rt.h"

int		sphere_isptin(t_pt pt, t_obj o)
{
	// printf("isin? dist: %f, r: %f\n", dist(pt, o.t), o.v[0]);
	return (dist(pt, o.t) <= o.v[0] + 0.000001);
}
