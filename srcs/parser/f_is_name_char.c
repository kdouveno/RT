#include "rt.h"

int	is_name_char(char c)
{
	return (c == '_' || c == '-' || c == ',' || c == '~'
	|| (c >= 'a' && c <= 'z')
	|| (c >= 'A' && c <= 'Z')
	|| (c >= '0' && c <= '9')
	|| c == '.' || c == '#');
}
